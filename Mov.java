public class Mov {
    public static int add(int a, int b) {
        return a + b;
    }

    public static int add(int a, int b, int c) {
        return a + b + c;
    }

    public static void main(String[] args) {
        System.out.println(add(15, 70));
        System.out.println(add(10, 20, 45));

    }
}
