//the final keyword in java is a non -access modifier used to impose restrictions  on variables.
//final variable
/*  A variable declared as final cannot have its value changed once initialized.
Commonly used for constants.*/


public class Finalvar {
    public static void main(String[] args) {
       final double PI =3.14;
        System.out.println("value of PI" + PI);
    }
    
}
